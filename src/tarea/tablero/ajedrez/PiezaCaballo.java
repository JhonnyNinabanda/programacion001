package tarea.tablero.ajedrez;

public class PiezaCaballo extends Piezas {

	private String caballoBlanca;
	private String caballoNegra;
	
	public String getCaballoBlanca() {
		return caballoBlanca;
	}
	public void setCaballoBlanca(String caballoBlanca) {
		this.caballoBlanca = caballoBlanca;
	}
	
	public String getCaballoNegra() {
		return caballoNegra;
	}
	public void setCaballoNegra(String caballoNegra) {
		this.caballoNegra = caballoNegra;
	}
}
